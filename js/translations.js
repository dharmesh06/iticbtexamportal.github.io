/**
 * Returns localized question object. Replaces text inside `q` with translated variants if available.
 */
function getLocalizedQuestion(q, langConfig, bankName) {
  if (!langConfig || langConfig === "English" || !TRANSLATIONS[langConfig]) {
    return q; // native english
  }

  // If the bank or question id is completely missing in the chosen language, show a prefixed English version.
  if (!TRANSLATIONS[langConfig][bankName] || !TRANSLATIONS[langConfig][bankName][q.id]) {
    return {
      ...q,
      question: `[${langConfig} Translation Pending] ` + q.question
    };
  }
  
  const translated = TRANSLATIONS[langConfig][bankName][q.id];
  return {
    ...q,
    question: translated.question,
    options: translated.options,
    explanation: translated.explanation
  };
}
